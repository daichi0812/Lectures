package p3;

public class MathTest {

	public static void main(String[] args) {
		System.out.println("sin30°: " + Math.sin(30 * 3.14159265 / 180));
		System.out.println("cos60°: " + Math.cos(60 * 3.14159265 / 180));
		System.out.println("tan45°: " + Math.tan(45 * 3.14159265 / 180));
		System.out.println("log10(100): " + Math.log10(100.0));
		System.out.println("pow(2.0, 5.0): " + Math.pow(2.0, 5.0));
	}

}
